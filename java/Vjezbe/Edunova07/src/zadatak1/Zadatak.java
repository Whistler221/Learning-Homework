package zadatak1;

public class Zadatak extends Zadatak1 {
	
	private int Here, We, Go;
	private boolean Again;
		
	public Zadatak() {
		
	}
	
	


	public boolean isAgain() {
		return Again;
	}




	public void setAgain(boolean again) {
		Again = again;
	}




	public int getHere() {
		return Here;
	}


	public void setHere(int here) {
		Here = here;
	}


	public int getWe() {
		return We;
	}


	public void setWe(int we) {
		We = we;
	}


	public int getGo() {
		return Go;
	}


	public void setGo(int go) {
		Go = go;
	}

	
	  
}