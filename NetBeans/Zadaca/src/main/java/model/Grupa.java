package model;

import javax.persistence.Entity;
import javax.persistence.Id;

/**
 *
 * @author filip
 */

@Entity
public class Grupa {
    
    @Id
    private int sifra;
    private String naziv;
    
    
    
    public int getSifra() {
        return sifra;
    }

    public void setSifra(int sifra) {
        this.sifra = sifra;
    }

    public String getNaziv() {
        return naziv;
    }

    public void setNaziv(String naziv) {
        this.naziv = naziv;
    }
    
    
    
}
