package model;

import javax.persistence.Entity;
import javax.persistence.Id;

/**
 *
 * @author filip
 */

@Entity
public class Predavac {
    

    @Id
    private int sifra;
    private String iban;

    public int getSifra() {
        return sifra;
    }

    public void setSifra(int sifra) {
        this.sifra = sifra;
    }

    public String getIban() {
        return iban;
    }

    public void setIban(String iban) {
        this.iban = iban;
    }
    
    
    
}
